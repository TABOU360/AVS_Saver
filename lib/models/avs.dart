class Avs {
  final String id;
  final String name;
  final double rating;
  final List<String> skills;
  final double hourlyRate;
  final bool verified;
  final String bio;
  Avs({
    required this.id,
    required this.name,
    required this.rating,
    required this.skills,
    required this.hourlyRate,
    required this.verified,
    required this.bio,
  });
}
